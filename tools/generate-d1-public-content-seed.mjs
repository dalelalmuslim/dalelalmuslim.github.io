import fs from 'node:fs/promises';
import path from 'node:path';
import crypto from 'node:crypto';
import { fileURLToPath } from 'node:url';
import { APP_CONFIG } from '../js/app/app-config.js';
import {
    getAzkarCatalog,
    getDailyMessages,
    getDuasCatalog,
    getStoriesCatalog
} from '../js/services/content/content-provider-local.js';
import {
    PUBLIC_CONTENT_DEFAULT_VERSIONS,
    PUBLIC_CONTENT_SECTIONS
} from '../js/shared/contracts/public-content-manifest.js';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const rootDir = path.resolve(__dirname, '..');
const outputDir = path.join(rootDir, 'd1/seed');
const ayahsJsonPath = path.join(rootDir, 'data/home/home-ayahs.json');

function stableStringify(value) {
    return JSON.stringify(value);
}

function hashPayload(value) {
    return crypto.createHash('sha256').update(stableStringify(value)).digest('hex');
}

function escapeSqlText(value) {
    return String(value).replace(/'/g, "''");
}

async function resolvePayload(sectionId) {
    switch (sectionId) {
        case 'app_config':
            return {
                appId: APP_CONFIG.APP_ID,
                appNameAr: APP_CONFIG.APP_NAME_AR,
                appNameEn: APP_CONFIG.APP_NAME_EN,
                appVersion: APP_CONFIG.APP_VERSION,
                schemaVersion: APP_CONFIG.SCHEMA_VERSION
            };
        case 'azkar':
            return getAzkarCatalog();
        case 'duas':
            return getDuasCatalog();
        case 'stories':
            return getStoriesCatalog();
        case 'daily_content': {
            const ayahsPayload = JSON.parse(await fs.readFile(ayahsJsonPath, 'utf8'));
            return {
                messages: getDailyMessages(),
                ayahs: Array.isArray(ayahsPayload) ? ayahsPayload : []
            };
        }
        default:
            return null;
    }
}

async function buildSeedSnapshot() {
    const generatedAt = new Date().toISOString();
    const documents = [];

    for (const section of PUBLIC_CONTENT_SECTIONS) {
        const payload = await resolvePayload(section.id);
        if (payload === null || payload === undefined) {
            throw new Error(`Missing seed payload for section: ${section.id}`);
        }

        const version = PUBLIC_CONTENT_DEFAULT_VERSIONS[section.id] || `${section.id}-seed-v1`;
        documents.push({
            sectionId: section.id,
            version,
            payload,
            payloadHash: hashPayload(payload),
            schemaVersion: 'public-content-v1',
            sourceKind: 'seed-import',
            publishedAt: generatedAt
        });
    }

    return {
        generatedAt,
        schemaVersion: 'public-content-v1',
        documents
    };
}

function createSeedSql(snapshot) {
    const statements = [
        '-- Generated by tools/generate-d1-public-content-seed.mjs',
        'BEGIN TRANSACTION;'
    ];

    for (const document of snapshot.documents) {
        const payloadJson = stableStringify(document.payload);
        statements.push(`INSERT OR REPLACE INTO public_content_documents (section_id, version, payload_json, payload_hash, schema_version, source_kind, created_at) VALUES ('${escapeSqlText(document.sectionId)}', '${escapeSqlText(document.version)}', '${escapeSqlText(payloadJson)}', '${escapeSqlText(document.payloadHash)}', '${escapeSqlText(document.schemaVersion)}', '${escapeSqlText(document.sourceKind)}', '${escapeSqlText(snapshot.generatedAt)}');`);
        statements.push(`INSERT OR REPLACE INTO public_content_publications (section_id, version, published_at, published_by, notes) VALUES ('${escapeSqlText(document.sectionId)}', '${escapeSqlText(document.version)}', '${escapeSqlText(document.publishedAt)}', 'seed-import', 'Initial public content seed from local baseline.');`);
    }

    statements.push('COMMIT;');
    statements.push('');
    return statements.join('\n');
}

async function main() {
    const snapshot = await buildSeedSnapshot();
    const seedJsonPath = path.join(outputDir, 'public-content.seed.json');
    const seedSqlPath = path.join(outputDir, '0001_public_content_seed.sql');

    await fs.mkdir(outputDir, { recursive: true });
    await fs.writeFile(seedJsonPath, JSON.stringify(snapshot, null, 2));
    await fs.writeFile(seedSqlPath, createSeedSql(snapshot));

    console.log(JSON.stringify({
        ok: true,
        generatedAt: snapshot.generatedAt,
        documents: snapshot.documents.map((document) => ({
            sectionId: document.sectionId,
            version: document.version,
            payloadHash: document.payloadHash
        })),
        outputFiles: [
            path.relative(rootDir, seedJsonPath),
            path.relative(rootDir, seedSqlPath)
        ]
    }, null, 2));
}

main().catch((error) => {
    console.error('[generate-d1-public-content-seed] Failed to generate seed files.');
    console.error(error);
    process.exitCode = 1;
});
